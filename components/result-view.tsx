"use client";

import * as React from "react";
import { Tabs, TabsList, TabsTrigger, TabsContent } from "@/components/ui/tabs";
import { CopyButton } from "@/components/copy-button";
import { DownloadButton } from "@/components/download-button";
import { AiProseDialog } from "@/components/ai-prose-dialog";
import { AiProseSection } from "@/components/ai-prose-section";
import { TokenPreview } from "@/lib/render/preview";
import { renderMarkdown } from "@/lib/render/markdown";
import { renderTokensJsonString } from "@/lib/render/tokens-json";
import type { ExtractedTokens } from "@/lib/types";

export function ResultView({ tokens }: { tokens: ExtractedTokens }) {
  const markdown = React.useMemo(() => renderMarkdown(tokens), [tokens]);
  const json = React.useMemo(() => renderTokensJsonString(tokens), [tokens]);
  const filenameBase = tokens.meta.hostname.replace(/\./g, "-");

  return (
    <section className="w-full max-w-6xl mx-auto px-6 py-8 space-y-6">
      <header className="flex flex-col sm:flex-row sm:items-end sm:justify-between gap-3 border-b pb-4">
        <div className="space-y-1">
          <p className="text-xs uppercase tracking-wider text-muted-fg font-mono">
            Result for
          </p>
          <h1 className="text-2xl font-semibold tracking-tight">
            {tokens.meta.name}
          </h1>
          <p className="text-sm text-muted-fg">
            <a
              href={tokens.meta.url}
              target="_blank"
              rel="noreferrer noopener"
              className="hover:underline"
            >
              {tokens.meta.hostname}
            </a>
            {" · "}
            {tokens.colors.primary.length +
              tokens.colors.neutral.length +
              tokens.colors.semantic.length}{" "}
            colours · {tokens.typography.roles.length} type roles ·{" "}
            {tokens.spacing.length} spacing · {tokens.radius.length} radii
          </p>
        </div>
        <div className="flex flex-wrap gap-2">
          <AiProseDialog />
          <CopyButton value={markdown} label="Copy Markdown" />
          <DownloadButton
            filename={`${filenameBase}.DESIGN.md`}
            content={markdown}
            label="Download .md"
          />
        </div>
      </header>

      <Tabs defaultValue="preview" className="space-y-4">
        <TabsList>
          <TabsTrigger value="preview">Preview</TabsTrigger>
          <TabsTrigger value="markdown">Markdown</TabsTrigger>
          <TabsTrigger value="json">Tokens JSON</TabsTrigger>
        </TabsList>

        <TabsContent value="preview" className="space-y-8">
          <TokenPreview tokens={tokens} />
          <div className="space-y-3">
            <h3 className="text-sm font-semibold tracking-tight">
              Optional AI prose
            </h3>
            <p className="text-xs text-muted-fg">
              The deterministic Markdown above stands on its own. These extra paragraphs are generated by your chosen LLM, on demand, with your own key.
            </p>
            <AiProseSection section="overview" tokens={tokens} />
            <AiProseSection section="components" tokens={tokens} />
            <AiProseSection section="guidelines" tokens={tokens} />
          </div>
        </TabsContent>

        <TabsContent value="markdown">
          <pre className="rounded-lg border bg-muted/40 p-4 overflow-x-auto text-xs leading-relaxed font-mono whitespace-pre-wrap">
            {markdown}
          </pre>
        </TabsContent>

        <TabsContent value="json">
          <pre className="rounded-lg border bg-muted/40 p-4 overflow-x-auto text-xs leading-relaxed font-mono whitespace-pre-wrap">
            {json}
          </pre>
        </TabsContent>
      </Tabs>

      {tokens.diagnostics.warnings.length > 0 && (
        <aside className="rounded-md border border-yellow-300 bg-yellow-50 p-3 text-xs text-yellow-900 dark:border-yellow-800 dark:bg-yellow-950/30 dark:text-yellow-200">
          <strong>Warnings</strong>
          <ul className="mt-1 list-disc list-inside space-y-0.5">
            {tokens.diagnostics.warnings.map((w, i) => (
              <li key={i}>{w}</li>
            ))}
          </ul>
        </aside>
      )}
    </section>
  );
}
